#ifndef __XILINX_DMA_H
#define __XILINX_DMA_H

/*********************************************************************************************************
  板级参数结构（由 dma.c 定义并传入 xilinx_dma_params_register）
*********************************************************************************************************/

typedef struct {
    CPCHAR      compatible;
    phys_addr_t reg_base;
    size_t      reg_size;
    struct {
        CPCHAR  compatible;
        INT     irq;
        UINT32  dma_channels;
    } child[2];
    UINT32      xlnx_datawidth;
    UINT32      xlnx_addrwidth;
} xilinx_board_params_t;

/*********************************************************************************************************
  两阶段初始化接口
*********************************************************************************************************/

INT  xilinx_dma_lib_init        (VOID);
INT  xilinx_dma_params_register (const xilinx_board_params_t *params);

#endif  /* __XILINX_DMA_H */
