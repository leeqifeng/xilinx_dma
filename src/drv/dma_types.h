#ifndef DMA_TYPES_H_
#define DMA_TYPES_H_

// #include "os_common.h"

/*********************************************************************************************************
  DMA 通道传输方向
*********************************************************************************************************/

#define DMA_DIR_MM2S        0                                           /*  内存到流 (发送/TX)          */
#define DMA_DIR_S2MM        1                                           /*  流到内存 (接收/RX)          */

/*********************************************************************************************************
  DMA 传输模式
*********************************************************************************************************/

#define DMA_MODE_SIMPLE     0                                           /*  简单模式：单段连续缓冲区    */
#define DMA_MODE_SG         1                                           /*  分散聚集模式：BD 链         */

/*********************************************************************************************************
  DMA 描述符状态码
*********************************************************************************************************/

#define DMA_STATUS_IDLE         0                                       /*  空闲，尚未提交              */
#define DMA_STATUS_PENDING      1                                       /*  已提交，等待调度            */
#define DMA_STATUS_ACTIVE       2                                       /*  正在传输                    */
#define DMA_STATUS_COMPLETE     3                                       /*  传输成功完成                */
#define DMA_STATUS_ERROR        (-1)                                    /*  传输发生错误                */

/*********************************************************************************************************
  完成回调函数类型
  注意：回调在中断上下文中调用，必须轻量级（如仅释放信号量），禁止阻塞。
*********************************************************************************************************/

typedef void (*dma_complete_cb_t)(void *arg, int status);

/*********************************************************************************************************
  分散聚集条目（虚拟地址，驱动内部转换为物理地址）
*********************************************************************************************************/

struct dma_sg {
    void   *buf;                                                        /*  缓冲区虚拟地址              */
    size_t  len;                                                        /*  缓冲区字节长度              */
};

/*********************************************************************************************************
  前向声明
*********************************************************************************************************/

struct dma_device;
struct dma_chan;
struct dma_desc;

/*********************************************************************************************************
  硬件操作表 —— 由控制器驱动层实现，由核心层和适配层调用
*********************************************************************************************************/

struct dma_ops {
    int  (*chan_init)(struct dma_chan *chan);                            /*  通道初始化（软复位等）      */
    int  (*chan_start)(struct dma_chan *chan);                           /*  启动通道（置位 RS）         */
    int  (*chan_stop)(struct dma_chan *chan);                            /*  停止通道（清除 RS）         */

    int  (*prep_simple)(struct dma_desc *desc,
                        void *dst, void *src, size_t len);              /*  准备简单模式描述符          */
    int  (*prep_sg)(struct dma_desc *desc,
                    struct dma_sg *sgl, int sg_len);                    /*  准备 SG 模式描述符          */

    int  (*issue_pending)(struct dma_chan *chan);                        /*  触发硬件传输                */
    int  (*tx_status)(struct dma_chan *chan, struct dma_desc *desc);     /*  查询传输状态                */

    void (*irq_handler)(struct dma_chan *chan);                          /*  中断处理（可选）            */
    void (*desc_free)(struct dma_desc *desc);                           /*  释放 hw_desc 资源           */
};

/*********************************************************************************************************
  DMA 通道 —— 由核心层管理
*********************************************************************************************************/

struct dma_chan {
    struct dma_device   *dev;                                           /*  所属设备                    */
    int                  id;                                            /*  通道编号                    */
    PLW_LIST_LINE        pending_q;                                     /*  待调度队列头                */
    PLW_LIST_LINE        active_q;                                      /*  传输中队列头                */
    spinlock_t           lock;                                          /*  队列保护锁（IRQ 安全）      */
    BOOL                 in_use;                                        /*  通道是否已被分配            */
    void                *priv;                                          /*  硬件私有数据指针            */
};

/*********************************************************************************************************
  DMA 描述符 —— 一次传输请求
*********************************************************************************************************/

struct dma_desc {
    struct dma_chan      *chan;                                          /*  所属通道                    */
    void                 *hw_desc;                                      /*  硬件私有描述符              */
    dma_complete_cb_t     cb;                                           /*  完成回调                    */
    void                 *cb_arg;                                       /*  回调参数                    */
    int                   status;                                       /*  当前状态 DMA_STATUS_*       */
    LW_LIST_LINE          node;                                         /*  链入 pending_q / active_q   */
};

/*********************************************************************************************************
  DMA 设备 —— 一个控制器，可含多个通道
*********************************************************************************************************/

struct dma_device {
    const char          *name;                                          /*  设备名，供 dma_request_chan */
    struct dma_ops      *ops;                                           /*  硬件操作表                  */
    struct dma_chan      *channels;                                     /*  通道数组                    */
    int                  num_channels;                                  /*  通道数量                    */
    LW_LIST_LINE         node;                                          /*  全局设备链表节点            */
    void                *priv;                                          /*  硬件私有设备数据            */
};

#endif                                                                  /*  DMA_TYPES_H_                */
