#ifndef DMA_CLIENT_H_
#define DMA_CLIENT_H_

#include "dma_types.h"

/*********************************************************************************************************
  通道管理
*********************************************************************************************************/

/*
 * dma_request_chan - 按名称申请一个空闲通道
 * 找到设备并分配第一个未占用通道；失败返回 NULL。
 */
struct dma_chan *dma_request_chan(const char *name);

/*
 * dma_release_chan - 释放通道
 * 终止所有传输，对在途描述符发出 DMA_STATUS_ERROR 回调，然后归还通道。
 */
void dma_release_chan(struct dma_chan *chan);

/*********************************************************************************************************
  传输准备
  两个函数均在成功时返回描述符指针，失败返回 NULL。
  描述符在提交并启动后由核心层管理生命周期；回调触发后描述符自动释放，
  调用方不得在回调返回后继续使用描述符指针。

  dma_prep_simple：单段连续缓冲区传输。
    MM2S 通道：src 为内存地址，dst 忽略。
    S2MM 通道：dst 为内存地址，src 忽略。

  dma_prep_sg：分散聚集传输。
    sgl[].buf 须指向 DMA 一致性内存（DMA zone）或已 Cache flush 的普通内存。
*********************************************************************************************************/

struct dma_desc *dma_prep_simple(struct dma_chan *chan,
                                 void *dst, void *src, size_t len,
                                 dma_complete_cb_t cb, void *arg);

struct dma_desc *dma_prep_sg(struct dma_chan *chan,
                              struct dma_sg *sgl, int sg_len,
                              dma_complete_cb_t cb, void *arg);

/*********************************************************************************************************
  提交与启动
  须先调用 dma_submit() 再调用 dma_issue_pending()。
  连续提交多个描述符后调用一次 dma_issue_pending() 即可。
*********************************************************************************************************/

int dma_submit(struct dma_desc *desc);
int dma_issue_pending(struct dma_chan *chan);

/*********************************************************************************************************
  状态与控制
  dma_tx_status ：查询描述符当前状态（DMA_STATUS_*）。
                  仅在回调触发前（传输仍在进行时）调用有意义。
  dma_terminate ：立即停止通道，丢弃所有 pending 和 active 传输，
                  对每个在途描述符以 DMA_STATUS_ERROR 调用回调。
*********************************************************************************************************/

int dma_tx_status(struct dma_chan *chan, struct dma_desc *desc);
int dma_terminate(struct dma_chan *chan);

#endif                                                                  /*  DMA_CLIENT_H_               */
